package hw2;

public class Instructor {

	int instructorId;
	String instructorName;
	
	public Instructor() {
		instructorId = 0;
		instructorName = null;
	}
	
	public Instructor(int instructorId , String instructorName) {
		this.instructorId = instructorId ;
		this.instructorName = instructorName;
	}
}
